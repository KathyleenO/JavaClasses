package com.class7;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int num=10;
		do {
			System.out.println("bye");
			num++;
		}while(num<+7);
		
		
		// Print even numbers from 20-50 using Do while
		
		
		int num1=20;
		
		do {
			System.out.println(num1);
			num1+=2;
			
		}while(num1<50);
	
		System.out.println("****************");
// If condition 
		
		int even=20;
		do {
			if(even%2==0) {
				System.out.println();
			}
		even++;
		}while (even<=50);
		
		
		
		
		
		
	}

}
